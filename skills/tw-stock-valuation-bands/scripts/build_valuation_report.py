#!/usr/bin/env python3
from __future__ import annotations

import argparse
import datetime
import json
import math
import subprocess
import sys
from dataclasses import dataclass
from pathlib import Path


DEFAULT_PER_BANDS = {
    5: (None, 8.0),
    4: (8.0, 9.0),
    3: (9.0, 10.0),
    2: (10.0, 11.0),
    1: (11.0, None),
}


@dataclass(frozen=True)
class Scenario:
    label: str
    eps: float


@dataclass(frozen=True)
class PriceHistorySummary:
    start_date: str
    end_date: str
    trading_days: int
    close_low: float
    close_high: float
    latest_close: float
    latest_close_date: str
    range_position_pct: float
    percentile_pct: float


