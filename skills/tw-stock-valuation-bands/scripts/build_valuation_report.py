def resolve_active_file(vault_root: Path) -> str | None:
    """讀 .obsidian/workspace.json 的 active leaf，回傳相對 vault 的 .md 路徑。"""
    ws = vault_root / ".obsidian" / "workspace.json"
    if not ws.is_file():
        return None
    try:
        data = json.loads(ws.read_text(encoding="utf-8"))
    except Exception:
        return None
    active = data.get("active")
    found: list[str | None] = [None]

    def walk(node: object) -> None:
        if isinstance(node, dict):
            if node.get("type") == "leaf" and node.get("id") == active:
                state = node.get("state", {})
                if state.get("type") == "markdown":
                    found[0] = state.get("state", {}).get("file")
            for value in node.values():
                walk(value)
        elif isinstance(node, list):
            for value in node:
                walk(value)

    for key in ("main", "left", "right"):
        walk(data.get(key, {}))
    result = found[0]
    if result and result.endswith(".md"):
        return result
    return None


def unique_path(base: Path) -> Path:
    """若 base 已存在，於檔名尾端加「-2」「-3」…回傳不重複的新路徑。"""
    if not base.exists():
        return base
    stem = base.stem
    n = 2
    while True:
        candidate = base.with_name(f"{stem}-{n}{base.suffix}")
        if not candidate.exists():
            return candidate
        n += 1


def write_obsidian_note(path: Path, content: str, mode: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if mode == "append" and path.exists() and path.stat().st_size > 0:
        lines = content.splitlines()
        count = 0
        body_start = 0
        for index, line in enumerate(lines):
            if line == "---":
                count += 1
                if count == 2:
                    body_start = index + 1
                    break
        body = "\n".join(lines[body_start:]) if count >= 2 else content
        with path.open("a", encoding="utf-8") as handle:
            handle.write("\n" + body.rstrip("\n") + "\n")
    else:
        path.write_text(content if content.endswith("\n") else content + "\n", encoding="utf-8")


def main() -> int:
    args = parse_args()
    analysis_json = args.analysis_json
    if args.stock_id:
        if args.analyze_script is None:
            print(
                "錯誤：提供 --stock-id 時，必須另外提供 --analyze-script。"
                " 目前 repo 沒有預設的 analyze_tw_stock.py，請先確認資料來源或直接提供 --analysis-json",
                file=sys.stderr,
            )
            return 1
        analysis_json = run_analysis(args.stock_id, args.analyze_script)
    if analysis_json is None:
        print("錯誤：請提供 --analysis-json 或 --stock-id", file=sys.stderr)
        return 1

    data = load_analysis_json(analysis_json)
    try:
        base_eps_value, _ = resolve_base_eps(data)
    except ValueError as exc:
        print(f"錯誤：{exc}", file=sys.stderr)
        return 1

    price_history = load_price_history_json(args.price_history_json, data.get("stock_id"))
    scenarios = build_scenarios(args, base_eps_value)
    report = make_report(data, scenarios, args.current_price, price_history)
    if args.output_format == "json":
        rendered_output = json.dumps(report, ensure_ascii=False, indent=2)
    else:  # md
        rendered_output = render_markdown(report)

    # md 且未指定輸出檔／輸出資料夾 → 預設寫入 Obsidian 當前開啟的檔案
    if (
        args.output_format == "md"
        and args.output_file is None
        and args.output_dir is None
    ):
        company = report["company_name"]
        stock_id = report["stock_id"]
        write_mode = args.write_mode
        if write_mode == "newfile":
            # 不動既有檔，改用預設命名建立不重複的新筆記
            # 檔名帶「估值」後綴，與 tw-stock-analysis 的「{代碼}-{名稱}.md」區隔，避免撞名
            output_path = unique_path(args.vault_root / f"{stock_id}-{company}-估值.md")
            target_hint = "建立新檔案"
            write_mode = "overwrite"
        elif args.note_path:
            output_path = args.vault_root / args.note_path
            target_hint = "指定路徑"
        else:
            active_rel = resolve_active_file(args.vault_root)
            if active_rel:
                output_path = args.vault_root / active_rel
                target_hint = "Obsidian 當前開啟檔案"
            else:
                output_path = args.vault_root / f"{stock_id}-{company}-估值.md"
                target_hint = "未偵測到當前檔案，改建新筆記"
        write_obsidian_note(output_path, rendered_output, write_mode)
        print(f"{output_path}（{target_hint}，{args.write_mode}）")
        return 0

    output_path = derive_output_path(
        analysis_json,
        args.output_format,
        args.output_file,
        args.output_dir,
    )
    write_report(output_path, rendered_output)
    print(output_path)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
